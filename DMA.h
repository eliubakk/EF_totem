/*
 * DMA Control Block in Main Memory
 *
 * Note: Must start at a 256 bit aligned address.
 *       Use corresponding register field definitions.
 */
typedef struct
{
    uint32_t ti;
    uint32_t source_ad;
    uint32_t dest_ad;
    uint32_t txfr_len;
    uint32_t stride;
    uint32_t nextconbk;
    uint32_t reserved[2];
} __attribute__((packed, aligned(4))) dma_cb_t;

